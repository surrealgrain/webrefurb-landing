    low_confidence = [
        _queue_item(item, reason="draft label starts low confidence until operator completes evidence review")
        for item in sample_items
    ]
    return {
        "expected_ready_second_pass_required": expected_ready,
        "low_confidence_drafts": low_confidence,
        "pipeline_disagreement_after_replay": [],
    }


