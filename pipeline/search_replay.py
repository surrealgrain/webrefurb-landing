def load_replay_corpus(corpus_dir: str | Path, *, require_labels: bool = True) -> dict[str, Any]:
    """Load a deterministic search replay fixture from disk.

    The first production-simulation slice uses committed, sanitized fixture
    records instead of live Serper/search/fetch calls. This keeps replay stable
    and makes the no-send boundary explicit.
    """
    root = Path(corpus_dir)
    manifest = read_json(root / "manifest.json")
    if not isinstance(manifest, dict):
        raise ReplayCorpusError(f"missing manifest.json in {root}")

    candidates_file = root / str(manifest.get("candidates_file") or "candidates.json")
    candidates = read_json(candidates_file)
    if not isinstance(candidates, list):
        raise ReplayCorpusError(f"missing candidate list: {candidates_file}")

    labels_dir = root / str(manifest.get("labels_dir") or "labels")
    labels: dict[str, dict[str, Any]] = {}
    label_paths = sorted(labels_dir.glob("*.json")) if labels_dir.exists() else []
    for path in label_paths:
        label = read_json(path)
        if not isinstance(label, dict):
            raise ReplayCorpusError(f"invalid label JSON: {path}")
        validate_label_schema(label, label_path=path)
        candidate_id = str(label["candidate_id"])
        labels[candidate_id] = label

    candidate_ids = {candidate_id_for(candidate) for candidate in candidates}
    missing_labels = sorted(candidate_ids - set(labels))
    if require_labels and missing_labels:
        partial_labels_allowed = bool(manifest.get("labeling_workflow")) or str(manifest.get("run_scope") or "") == "pilot_broad_collection_scaffold"
        if partial_labels_allowed and labels:
            candidates = [candidate for candidate in candidates if candidate_id_for(candidate) in labels]
        else:
            raise ReplayCorpusError(f"missing labels for candidates: {missing_labels}")

    return {
        "root": root,
        "manifest": manifest,
        "candidates": candidates,
        "labels": labels,
        "unlabeled_candidate_ids": missing_labels,
    }


def validate_label_schema(label: dict[str, Any], *, label_path: str | Path | None = None) -> None:
    """Validate finalized production-simulation labels before replay.

    Drafts generated by the labeling workflow live outside the manifest
    labels directory. Anything placed directly in labels/ is treated as
    replayable ground truth, so expected-ready labels must be high confidence
    or carry an approved second-pass review.
    """
    name = Path(label_path).name if label_path else str(label.get("candidate_id") or "label")
    missing = REQUIRED_LABEL_FIELDS - set(label)
    if missing:
        raise ReplayCorpusError(f"label {name} missing fields: {sorted(missing)}")
    _assert_label_value(name, "category_expected", label.get("category_expected"), LABEL_CATEGORY_VALUES)
    _assert_label_value(name, "readiness_expected", label.get("readiness_expected"), LABEL_READINESS_VALUES)
    _assert_label_value(name, "package_expected", label.get("package_expected"), LABEL_PACKAGE_VALUES)
    _assert_label_value(name, "contact_route_expected", label.get("contact_route_expected"), LABEL_CONTACT_ROUTE_VALUES)
    _assert_label_value(name, "ticket_machine_state_expected", label.get("ticket_machine_state_expected"), LABEL_TICKET_MACHINE_VALUES)
    _assert_label_value(name, "english_menu_state_expected", label.get("english_menu_state_expected"), LABEL_ENGLISH_MENU_VALUES)
    _assert_label_value(name, "proof_strength_minimum", label.get("proof_strength_minimum"), LABEL_PROOF_VALUES)
    _assert_label_value(name, "label_confidence", label.get("label_confidence"), LABEL_CONFIDENCE_VALUES)
    if not isinstance(label.get("inline_assets_expected"), list):
        raise ReplayCorpusError(f"label {name} inline_assets_expected must be a list")

    if str(label.get("readiness_expected")) == "ready_for_outreach":
        confidence = str(label.get("label_confidence") or "")
        review = label.get("second_pass_review") if isinstance(label.get("second_pass_review"), dict) else {}
        review_status = str((review or {}).get("status") or "").strip().lower()
        review_approved = review_status in {"approved", "complete", "completed"} or (review or {}).get("approved") is True
        if confidence != "high" and not review_approved:
            raise ReplayCorpusError(
                f"label {name} expected-ready labels require label_confidence=high "
                "or approved second_pass_review"
            )


def _assert_label_value(name: str, key: str, value: Any, allowed: set[str]) -> None:
    if str(value) not in allowed:
        raise ReplayCorpusError(f"label {name} {key} must be one of {sorted(allowed)}")


def reconcile_label_contact_policy(corpus_dir: str | Path) -> dict[str, Any]:
    """Rewrite stale finalized labels so unsupported routes no longer count as ready."""
    root = Path(corpus_dir)
    manifest = read_json(root / "manifest.json")
    if not isinstance(manifest, dict):
        raise ReplayCorpusError(f"missing manifest.json in {root}")
    labels_dir = root / str(manifest.get("labels_dir") or "labels")
    changed: list[dict[str, Any]] = []
    unchanged = 0
    for path in sorted(labels_dir.glob("*.json")):
        label = read_json(path)
        if not isinstance(label, dict):
            continue
        before = json.dumps(label, sort_keys=True, ensure_ascii=False)
        route = str(label.get("contact_route_expected") or "").strip()
        if route in UNSUPPORTED_LABEL_CONTACT_ROUTES:
            label["legacy_contact_route_expected"] = route
            label["contact_route_expected"] = "none"
            label["inline_assets_expected"] = []
            notes = str(label.get("label_notes") or "").strip()
            suffix = "Reconciled to email/contact-form-only policy; unsupported outreach routes require manual review."
            label["label_notes"] = f"{notes} {suffix}".strip()
            if label.get("readiness_expected") == "ready_for_outreach":
                label["readiness_expected"] = "manual_review"
                label["rejection_reason_expected"] = "no_supported_contact_route"
                label["package_expected"] = "none"
                review = label.get("second_pass_review") if isinstance(label.get("second_pass_review"), dict) else {}
                label["second_pass_review"] = {
                    **review,
                    "required": False,
                    "status": "not_required",
                    "reason": "route policy reconciliation moved unsupported-route ready label to manual review",
                }
        elif route not in LABEL_CONTACT_ROUTE_VALUES:
            label["legacy_contact_route_expected"] = route
            label["contact_route_expected"] = "none"
            label["inline_assets_expected"] = []
            if label.get("readiness_expected") == "ready_for_outreach":
                label["readiness_expected"] = "manual_review"
                label["rejection_reason_expected"] = "no_supported_contact_route"
                label["package_expected"] = "none"

        after = json.dumps(label, sort_keys=True, ensure_ascii=False)
        if after != before:
            validate_label_schema(label, label_path=path)
            write_json(path, label)
            changed.append({
                "candidate_id": label.get("candidate_id"),
                "business_name": label.get("business_name"),
                "legacy_contact_route_expected": label.get("legacy_contact_route_expected"),
                "contact_route_expected": label.get("contact_route_expected"),
                "readiness_expected": label.get("readiness_expected"),
            })
        else:
            validate_label_schema(label, label_path=path)
            unchanged += 1
    return {
        "corpus": str(root),
        "changed_count": len(changed),
        "unchanged_count": unchanged,
        "changed": changed,
    }


def candidate_id_for(candidate: dict[str, Any]) -> str:
    candidate_id = str(candidate.get("candidate_id") or candidate.get("lead_id") or "").strip()
    if not candidate_id:
        raise ReplayCorpusError(f"candidate is missing candidate_id/lead_id: {candidate}")
    return candidate_id


def materialize_replay_state(
    *,
    corpus: dict[str, Any],
    state_root: str | Path,
) -> list[dict[str, Any]]:
    """Write replay candidates as lead JSON under an isolated state root."""
    root = Path(state_root)
    leads_dir = root / "leads"
    leads_dir.mkdir(parents=True, exist_ok=True)
    materialized: list[dict[str, Any]] = []
    corpus_root = Path(corpus["root"])

    for candidate in corpus["candidates"]:
        if isinstance(candidate.get("record"), dict):
            record = _resolve_project_root_tokens(dict(candidate["record"]))
        else:
            record = _materialize_collected_candidate(root=corpus_root, candidate=candidate, state_root=root)
        candidate_id = candidate_id_for(candidate)
        record.setdefault("lead_id", candidate_id)
        record.setdefault("production_sim_candidate_id", candidate_id)
        record.setdefault("production_sim_fixture", True)
        record.setdefault("generated_at", "2026-04-29T00:00:00+00:00")
        record.setdefault("status_history", [{"status": record.get("outreach_status") or "new", "timestamp": "2026-04-29T00:00:00+00:00"}])
        record = ensure_lead_dossier(record)
        persist_lead_record(record, state_root=root)
        materialized.append(record)

    return materialized


def _materialize_collected_candidate(*, root: Path, candidate: dict[str, Any], state_root: Path) -> dict[str, Any]:
    """Replay a collected live candidate through offline qualification.

    Collected corpora store captured HTML/search artifacts rather than fully
    persisted lead records. This adapter keeps replay network-free while still
    exercising the production qualification, contact, package, proof, and asset
    selection paths.
    """
    from .pitch import build_pitch
    from .preview import build_preview_html, build_preview_menu
    from .qualification import qualify_candidate
    from .record import create_lead_record, normalise_lead_contacts
    from .business_name import business_name_is_suspicious

    candidate_id = candidate_id_for(candidate)
    search_job = dict(candidate.get("source_search_job") or {})
    pages = _captured_pages_for_candidate(root=root, candidate=candidate)
    category = str(candidate.get("category_hint") or search_job.get("category") or "")
    qualification = qualify_candidate(
        business_name=str(candidate.get("business_name") or ""),
        website=str(candidate.get("website") or ""),
        category=category,
        pages=[{"url": page["url"], "html": page["html"]} for page in pages],
        rating=candidate.get("rating"),
        reviews=candidate.get("rating_count") or candidate.get("reviews"),
        address=str(candidate.get("address") or ""),
        phone=str(candidate.get("phone") or ""),
        place_id=str(candidate.get("place_id") or ""),
        map_url=str(candidate.get("map_url") or ""),
    )
    solution_check_reject = qualification.lead and _search_job_marks_existing_solution(search_job)
    suspicious_name_reject = qualification.lead and business_name_is_suspicious(qualification.business_name)
    contacts = _captured_contact_routes(candidate=candidate, pages=pages)

    if qualification.lead and not solution_check_reject and not suspicious_name_reject:
        preview_menu = build_preview_menu(
            assessment=qualification,
            snippets=qualification.evidence_snippets,
            business_name=qualification.business_name,
        )
        preview_html = build_preview_html(
            preview_menu=preview_menu,
            ticket_machine_hint=None,
            business_name=qualification.business_name,
        )
        pitch = build_pitch(
            business_name=qualification.business_name,
            category=qualification.primary_category_v1,
            preview_menu=preview_menu,
            ticket_machine_hint=None,
            recommended_package=qualification.recommended_primary_package,
        )
        record = create_lead_record(
            qualification=qualification,
            preview_html=preview_html,
            pitch_draft=pitch,
            contacts=contacts,
            source_query=str(search_job.get("query") or ""),
            source_search_job=search_job,
            matched_friction_evidence=_replay_matched_friction_evidence(qualification=qualification, search_job=search_job),
            state_root=state_root,
        )
    else:
        if solution_check_reject:
            rejection_reason = "already_solved_solution_check"
        elif suspicious_name_reject:
            rejection_reason = "invalid_business_name_detected"
        else:
            rejection_reason = qualification.rejection_reason
        english_state = "usable_complete" if solution_check_reject else qualification.english_menu_state
        record = {
            **qualification.to_dict(),
            "lead": False,
            "rejection_reason": rejection_reason,
            "english_availability": "clear_usable" if solution_check_reject else qualification.english_availability,
            "english_menu_state": english_state,
            "lead_id": candidate_id,
            "business_name": qualification.business_name or str(candidate.get("business_name") or ""),
            "website": qualification.website or str(candidate.get("website") or ""),
            "address": qualification.address or str(candidate.get("address") or ""),
            "phone": qualification.phone or str(candidate.get("phone") or ""),
            "place_id": qualification.place_id or str(candidate.get("place_id") or ""),
            "map_url": qualification.map_url or str(candidate.get("map_url") or ""),
            "rating": candidate.get("rating"),
            "reviews": candidate.get("rating_count") or candidate.get("reviews"),
            "category": category,
            "source_query": str(search_job.get("query") or ""),
            "source_search_job": search_job,
            "matched_friction_evidence": _replay_matched_friction_evidence(qualification=qualification, search_job=search_job),
            "source_urls": {
                "website": qualification.website or str(candidate.get("website") or ""),
                "map_url": qualification.map_url or str(candidate.get("map_url") or ""),
                "evidence_urls": qualification.evidence_urls,
            },
            "contacts": normalise_lead_contacts({
                "contacts": contacts,
                "website": str(candidate.get("website") or ""),
                "map_url": str(candidate.get("map_url") or ""),
                "generated_at": "2026-04-29T00:00:00+00:00",
            }),
            "pitch_draft": {},
            "outreach_assets_selected": [],
            "outreach_asset_template_family": "none",
            "outreach_status": "new",
        }

    record["production_sim_candidate_id"] = candidate_id
    record["production_sim_fixture"] = True
    record["production_sim_capture_status"] = str((candidate.get("capture") or {}).get("status") or "")
    record["production_sim_artifact_refs"] = _candidate_artifact_refs(candidate)
    return record


def _captured_pages_for_candidate(*, root: Path, candidate: dict[str, Any]) -> list[dict[str, str]]:
    pages: list[dict[str, str]] = []
    for page in (candidate.get("capture") or {}).get("pages") or []:
        rel_path = str(page.get("path") or "")
        path = root / rel_path
        if not rel_path or not path.exists():
            continue
        pages.append({
            "source": str(page.get("source") or "captured_page"),
            "url": str(page.get("url") or candidate.get("website") or ""),
            "path": rel_path,
            "html": path.read_text(encoding="utf-8", errors="ignore"),
        })
    return pages


def _captured_contact_routes(*, candidate: dict[str, Any], pages: list[dict[str, str]]) -> list[dict[str, Any]]:
    from .contact_crawler import extract_contact_signals
    from .record import normalise_lead_contacts

    website = str(candidate.get("website") or "").strip()
    phone = str(candidate.get("phone") or "").strip()
    address = str(candidate.get("address") or "").strip()
    map_url = str(candidate.get("map_url") or "").strip()
    generated_at = "2026-04-29T00:00:00+00:00"
    raw_contacts: list[dict[str, Any]] = []
    seen: set[tuple[str, str]] = set()

    def append(
        contact_type: str,
        value: str,
        *,
        label: str = "",
        href: str = "",
        source: str = "",
        source_url: str = "",
        confidence: str = "medium",
        actionable: bool = True,
        metadata: dict[str, Any] | None = None,
    ) -> None:
        cleaned = str(value or "").strip()
        if not cleaned:
            return
        key_value = re.sub(r"\D", "", cleaned) if contact_type == "phone" else cleaned.lower()
        key = (contact_type, key_value)
        if not key_value or key in seen:
            return
        seen.add(key)
        contact = {
            "type": contact_type,
            "value": cleaned,
            "label": label or cleaned,
            "href": href,
            "source": source,
            "source_url": source_url,
            "confidence": confidence,
            "discovered_at": generated_at,
            "status": "discovered" if actionable else "reference_only",
            "actionable": actionable,
        }
        for key, item in (metadata or {}).items():
            if item not in (None, "", []):
                contact[key] = item
        raw_contacts.append(contact)

    if website:
        append("website", website, label="Official website", href=website, source="homepage", source_url=website, confidence="high", actionable=False)
    if phone:
        append("phone", phone, href=f"tel:{re.sub(r'\D', '', phone)}", source="maps_listing", source_url=map_url or website, confidence="high", actionable=False)
    if address:
        append("walk_in", address, label="Walk-in route", source="maps_listing", source_url=map_url or website, confidence="high", actionable=False)
    if map_url:
        append("map_url", map_url, label="Map listing", href=map_url, source="maps_listing", source_url=map_url, confidence="high", actionable=False)

    for page in pages:
        if not _page_can_supply_business_contact(website=website, page=page):
            continue
        signals = extract_contact_signals(page["html"])
        source = str(page.get("source") or "captured_page")
        source_url = str(page.get("url") or "")
        for email in signals.emails:
            append("email", email, href=f"mailto:{email}", source=source, source_url=source_url, confidence="high")
        if signals.has_form:
            append(
                "contact_form",
                source_url,
                label="Contact form",
                href=source_url,
                source=source,
                source_url=source_url,
                metadata={
                    "form_actions": signals.form_actions,
                    "required_fields": signals.required_fields,
                },
            )
    return normalise_lead_contacts({
        "contacts": raw_contacts,
        "website": website,
        "map_url": map_url,
        "generated_at": generated_at,
    })


def _page_can_supply_business_contact(*, website: str, page: dict[str, str]) -> bool:
    source = str(page.get("source") or "")
    if source not in {"homepage", "contact_page"}:
        return False
    return bool(_host_key(website) and _host_key(website) == _host_key(str(page.get("url") or "")))


def _host_key(url: str) -> str:
    host = urllib.parse.urlparse(str(url or "")).netloc.lower()
    if host.startswith("www."):
        host = host[4:]
    return host


def _replay_matched_friction_evidence(*, qualification: Any, search_job: dict[str, Any]) -> list[str]:
    evidence = [str(item) for item in qualification.evidence_classes or [] if str(item)]
    expected = str(search_job.get("expected_friction") or "")
    purpose = str(search_job.get("purpose") or "")
    if expected:
        evidence.append(expected)
    if purpose:
        evidence.append(purpose)
    return sorted(set(evidence))


def _search_job_marks_existing_solution(search_job: dict[str, Any]) -> bool:
    job_id = str(search_job.get("job_id") or "")
    purpose = str(search_job.get("purpose") or "")
    expected = str(search_job.get("expected_friction") or "")
    haystack = " ".join([job_id, purpose, expected])
    return any(token in haystack for token in (
        "english_menu_check",
        "english_solution_check",
        "english_ticket_machine_check",
        "mobile_order_check",
        "mobile_order_solution_check",
        "multilingual_qr_check",
        "multilingual_solution_check",
        "ticket_machine_solution_check",
    ))


def _resolve_project_root_tokens(value: Any) -> Any:
    if isinstance(value, str):
        return value.replace("{PROJECT_ROOT}", str(PROJECT_ROOT))
    if isinstance(value, list):
        return [_resolve_project_root_tokens(item) for item in value]
    if isinstance(value, dict):
        return {key: _resolve_project_root_tokens(item) for key, item in value.items()}
    return value


def copy_corpus_snapshot(
    *,
    corpus_dir: str | Path,
    replay_root: str | Path,
) -> None:
    """Copy the deterministic fixture into the runtime replay artifact path."""
    source = Path(corpus_dir)
    target = Path(replay_root)
    if source.resolve() == target.resolve():
        write_json(target / "source.json", {"source_corpus": str(source), "snapshot": "source_equals_target"})
        return
    if target.exists():
        shutil.rmtree(target)
    shutil.copytree(source, target)
    write_json(target / "source.json", {"source_corpus": str(source)})


def prepare_stratified_labeling_workflow(
    *,
    corpus_dir: str | Path,
    sample_size: int = 120,
    seed: str = "production-sim-labeling-v1",
    output_dir: str | Path | None = None,
) -> dict[str, Any]:
    """Create draft labels and review queues for a collected replay corpus.

    This intentionally writes draft labels outside the manifest labels/ folder.
    Finalized labels must be copied into labels/ only after operator review.
    """
    root = Path(corpus_dir)
    corpus = load_replay_corpus(root, require_labels=False)
    candidates = list(corpus["candidates"])
    if not candidates:
        raise ReplayCorpusError(f"corpus has no candidates: {root}")
    if sample_size < 1:
        raise ReplayCorpusError("sample_size must be at least 1")

    workflow_dir = Path(output_dir) if output_dir else root / "labeling"
    drafts_dir = workflow_dir / "drafts"
    workflow_dir.mkdir(parents=True, exist_ok=True)
    drafts_dir.mkdir(parents=True, exist_ok=True)
    for stale in drafts_dir.glob("*.json"):
        stale.unlink()

    annotations = [_annotate_label_candidate(root, candidate) for candidate in candidates]
    selected = _select_stratified_annotations(annotations, sample_size=min(sample_size, len(annotations)), seed=seed)
    drafts: list[dict[str, Any]] = []
    for annotation in selected:
        draft = _draft_label_for_annotation(annotation)
        drafts.append(draft)
        write_json(drafts_dir / f"{draft['candidate_id']}.json", draft)

    sample_items = [_sample_item_for_annotation(annotation) for annotation in selected]
    review_queue = _label_review_queue(sample_items)
    coverage = _label_sample_coverage(sample_items)
    template = _label_template()
