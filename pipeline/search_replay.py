def _place_name(place: dict[str, Any]) -> str:
    return str(place.get("title") or place.get("name") or "").strip()


def _dedupe_keys_for_place(place: dict[str, Any]) -> list[str]:
    keys: list[str] = []
    place_id = str(place.get("placeId") or "").strip()
    if place_id:
        keys.append(f"place_id:{place_id}")
    domain = _normalised_domain(str(place.get("website") or ""))
    if domain:
        keys.append(f"domain:{domain}")
    name_address = _normalised_name_address(_place_name(place), str(place.get("address") or ""))
    if name_address:
        keys.append(f"name_address:{name_address}")
    phone = _normalised_phone(str(place.get("phoneNumber") or place.get("phone") or ""))
    if phone:
        keys.append(f"phone:{phone}")
    if not keys:
        keys.append(f"fallback:{hashlib.sha1(str(place).encode('utf-8')).hexdigest()[:16]}")
    return list(dict.fromkeys(keys))


def _candidate_id_for_place(place: dict[str, Any]) -> str:
    basis = "|".join(_dedupe_keys_for_place(place))
    domain = _normalised_domain(str(place.get("website") or ""))
    slug_basis = domain.split(".")[0] if domain else _place_name(place)
    return f"wrm-replay-{slugify(slug_basis)[:40]}-{hashlib.sha1(basis.encode('utf-8')).hexdigest()[:8]}"


def _normalised_domain(url: str) -> str:
    parsed = urllib.parse.urlparse(str(url or "").strip())
    return parsed.netloc.lower().removeprefix("www.")


def _normalised_phone(phone: str) -> str:
    return "".join(ch for ch in str(phone or "") if ch.isdigit())


def _normalised_name_address(name: str, address: str) -> str:
    cleaned_name = " ".join(str(name or "").lower().split())
    cleaned_address = " ".join(str(address or "").lower().split())
    if not cleaned_name or not cleaned_address:
        return ""
    return f"{cleaned_name}|{cleaned_address}"


def _blocked_capture_link(link: str) -> bool:
    parsed = urllib.parse.urlparse(str(link or ""))
    host = parsed.netloc.lower().removeprefix("www.")
    return any(domain in host for domain in (
        "instagram.com",
        "facebook.com",
        "twitter.com",
        "x.com",
        "youtube.com",
        "tiktok.com",
        "pinterest.com",
    ))


