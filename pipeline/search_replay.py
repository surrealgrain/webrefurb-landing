def collect_replay_corpus(
    *,
    run_id: str,
    replay_root: str | Path,
    city_set: str = "launch-markets",
    cities: list[str] | None = None,
    category: str = "all",
    limit_per_job: int = 5,
    stage: str = "pilot",
    serper_api_key: str = "",
    gl: str = "jp",
    fetch_timeout_seconds: int = 8,
    contact_pages_per_candidate: int = 2,
    evidence_pages_per_candidate: int = 2,
    maps_search_fn: Callable[..., Any] | None = None,
    web_search_fn: Callable[..., Any] | None = None,
    fetch_page_fn: Callable[..., str] | None = None,
    created_at: str | None = None,
) -> dict[str, Any]:
    """Collect a stratified search/fetch corpus without touching lead state.

    The collector only writes replay artifacts under state/search-replay. It
    does not qualify leads, create launch batches, or touch any send path.
    Tests pass mocked search/fetch callables; CLI use can pass real Serper
    credentials for a no-send pilot/broad capture.
    """
    if not run_id:
        raise ReplayCorpusError("run_id is required for collection artifacts")
    created_at = created_at or utc_now()
    category = normalise_search_category(category)
    selected_cities = cities or cities_for_market_set(city_set)
    if not selected_cities:
        raise ReplayCorpusError("at least one city is required for production-sim collect")
    if limit_per_job < 1:
        raise ReplayCorpusError("limit_per_job must be at least 1")

    using_default_maps = maps_search_fn is None
    maps_search = maps_search_fn or _default_maps_search
    web_search = web_search_fn or _default_web_search
    fetch_page = fetch_page_fn or _default_fetch_page
    if using_default_maps and not serper_api_key:
        raise ReplayCorpusError("production-sim collect requires --api-key/SERPER_API_KEY or an offline fixture")

    root = Path(replay_root) / run_id
    serper_dir = root / "serper"
    pages_dir = root / "pages"
    labels_dir = root / "labels"
    for directory in (serper_dir, pages_dir, labels_dir):
        directory.mkdir(parents=True, exist_ok=True)

    jobs = collection_search_jobs(category=category, cities=selected_cities)
    candidates: list[dict[str, Any]] = []
    candidates_by_id: dict[str, dict[str, Any]] = {}
    seen_keys: dict[str, str] = {}
    duplicates: list[dict[str, Any]] = []
    fetch_failures: list[dict[str, Any]] = []
    search_failures: list[dict[str, Any]] = []
    raw_candidate_count = 0

    for job_index, job in enumerate(jobs):
        artifact_rel = f"serper/{job_index:04d}-{slugify(job['city'])}-{slugify(job['job_id'])}-maps.json"
        try:
            raw_response = _call_maps_search(
                maps_search,
                query=job["query"],
                api_key=serper_api_key,
                gl=gl,
                timeout_seconds=fetch_timeout_seconds,
            )
        except Exception as exc:
            raw_response = {"places": [], "error": f"{type(exc).__name__}: {exc}"}
            search_failures.append({
                "job_id": job["job_id"],
                "query": job["query"],
                "city": job["city"],
                "category": job["category"],
                "artifact": artifact_rel,
                "error_type": type(exc).__name__,
                "error": str(exc),
            })
        raw_response = _normalise_maps_response(raw_response)
        write_json(root / artifact_rel, {
            "artifact_type": "serper_maps_response",
            "captured_at": created_at,
            "gl": gl,
            "job": job,
            "response": raw_response,
        })

        for rank, place in enumerate((raw_response.get("places") or [])[:limit_per_job], start=1):
            raw_candidate_count += 1
            source_ref = {
                "job_id": job["job_id"],
                "query": job["query"],
                "city": job["city"],
                "category": job["category"],
                "rank": rank,
                "serper_artifact": artifact_rel,
            }
            keys = _dedupe_keys_for_place(place)
            duplicate_of = next((seen_keys[key] for key in keys if key in seen_keys), "")
            if duplicate_of:
                duplicate_record = {
                    "duplicate_of": duplicate_of,
                    "source_job": source_ref,
                    "matched_keys": [key for key in keys if key in seen_keys],
                    "business_name": _place_name(place),
                    "website": str(place.get("website") or ""),
                    "address": str(place.get("address") or ""),
                    "phone": str(place.get("phoneNumber") or place.get("phone") or ""),
                    "place_id": str(place.get("placeId") or ""),
                }
                duplicates.append(duplicate_record)
                if duplicate_of in candidates_by_id:
                    candidates_by_id[duplicate_of].setdefault("duplicate_source_jobs", []).append(source_ref)
                continue

            candidate_id = _candidate_id_for_place(place)
            for key in keys:
                seen_keys[key] = candidate_id
            candidate = {
                "candidate_id": candidate_id,
                "business_name": _place_name(place),
                "website": str(place.get("website") or ""),
                "address": str(place.get("address") or ""),
                "phone": str(place.get("phoneNumber") or place.get("phone") or ""),
                "place_id": str(place.get("placeId") or ""),
                "map_url": str(place.get("link") or place.get("mapUrl") or ""),
                "rating": place.get("rating"),
                "rating_count": place.get("ratingCount") or place.get("reviews"),
                "category_hint": job["category"],
                "source_search_job": job,
                "source_jobs": [source_ref],
                "duplicate_source_jobs": [],
                "dedupe_keys": keys,
                "capture": {
                    "status": "pending",
                    "serper_maps_artifacts": [artifact_rel],
                    "serper_web_artifacts": [],
                    "pages": [],
                    "fetch_failures": [],
                },
            }
            candidates.append(candidate)
            candidates_by_id[candidate_id] = candidate

    for candidate in candidates:
        _capture_candidate_pages(
            root=root,
            pages_dir=pages_dir,
            serper_dir=serper_dir,
            candidate=candidate,
            web_search_fn=web_search,
            fetch_page_fn=fetch_page,
            serper_api_key=serper_api_key,
            gl=gl,
            fetch_timeout_seconds=fetch_timeout_seconds,
            contact_pages_per_candidate=contact_pages_per_candidate,
            evidence_pages_per_candidate=evidence_pages_per_candidate,
            created_at=created_at,
            fetch_failures=fetch_failures,
        )

    write_json(root / "candidates.json", candidates)
    write_json(root / "duplicates.json", duplicates)
    write_json(root / "fetch-failures.json", fetch_failures)
    write_json(root / "search-failures.json", search_failures)

    fetch_success_count = sum(len((candidate.get("capture") or {}).get("pages") or []) for candidate in candidates)
    manifest = {
        "schema_version": COLLECTION_SCHEMA_VERSION,
        "run_id": run_id,
        "run_scope": "pilot_broad_collection_scaffold",
        "created_for": "PRODUCTION_SIMULATION_TEST_PLAN.md",
        "created_at": created_at,
        "stage": stage,
        "city_set": city_set,
        "cities": selected_cities,
        "category": category,
        "limit_per_job": limit_per_job,
        "gl": gl,
        "network": "mocked_offline_fixture" if not using_default_maps else "live_serper_and_fetch_capture",
        "external_send_allowed": False,
        "launch_batch_allowed": False,
        "offline_replay_compatible": True,
        "requires_labels_before_replay": True,
        "candidates_file": "candidates.json",
        "labels_dir": "labels",
        "serper_dir": "serper",
        "pages_dir": "pages",
        "duplicates_file": "duplicates.json",
        "fetch_failures_file": "fetch-failures.json",
        "search_failures_file": "search-failures.json",
        "search_job_count": len(jobs),
        "search_jobs": jobs,
        "raw_candidate_count": raw_candidate_count,
        "candidate_count": len(candidates),
        "duplicate_count": len(duplicates),
        "label_count": 0,
        "fetch_success_count": fetch_success_count,
        "fetch_failure_count": len(fetch_failures),
        "search_failure_count": len(search_failures),
        "strata": _collection_strata(jobs),
    }
    write_json(root / "manifest.json", manifest)
    return {
        "run_id": run_id,
        "root": str(root),
        "manifest_path": str(root / "manifest.json"),
        "manifest": manifest,
        "candidates": candidates,
        "duplicates": duplicates,
        "fetch_failures": fetch_failures,
        "search_failures": search_failures,
        "external_send_performed": False,
        "real_launch_batch_created": False,
    }


