def fixture_collect_adapters(fixture_path: str | Path) -> tuple[Callable[..., Any], Callable[..., Any], Callable[..., str]]:
    fixture = read_json(Path(fixture_path))
    if not isinstance(fixture, dict):
        raise ReplayCorpusError(f"invalid collection fixture: {fixture_path}")
    maps = fixture.get("maps") or {}
    web = fixture.get("web") or {}
    pages = fixture.get("pages") or {}
    failures = fixture.get("failures") or {}

    def maps_search(*, query: str, **_: Any) -> Any:
        return maps.get(query) or {"places": []}

    def web_search(*, query: str, **_: Any) -> Any:
        return web.get(query) or {"organic": []}

    def fetch_page(url: str, **_: Any) -> str:
        if url in failures:
            raise RuntimeError(str(failures[url]))
        if url not in pages:
            raise RuntimeError(f"fixture missing page: {url}")
        return str(pages[url])

    return maps_search, web_search, fetch_page


