def _candidate_label_text(root: Path, candidate: dict[str, Any], *, max_chars: int = 16000) -> str:
    parts = [
        str(candidate.get("business_name") or ""),
        str(candidate.get("website") or ""),
        str(candidate.get("address") or ""),
        str(candidate.get("phone") or ""),
        json.dumps(candidate.get("source_search_job") or {}, ensure_ascii=False),
    ]
    for page in (candidate.get("capture") or {}).get("pages") or []:
        rel = str(page.get("path") or "")
        path = root / rel
        if path.exists():
            raw = path.read_text(encoding="utf-8", errors="ignore")[:max_chars]
            parts.append(_html_to_label_text(raw))
    return " ".join(parts).lower()


def _html_to_label_text(html: str) -> str:
    text = re.sub(r"<(script|style)\b.*?</\1>", " ", html, flags=re.I | re.S)
    text = re.sub(r"<[^>]+>", " ", text)
    return re.sub(r"\s+", " ", text)


