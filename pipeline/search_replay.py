def _select_stratified_annotations(
    annotations: list[dict[str, Any]],
    *,
    sample_size: int,
    seed: str,
) -> list[dict[str, Any]]:
    selected: dict[str, dict[str, Any]] = {}
    dimensions = (
        ("market_category", lambda item: f"{item['market']}:{item['category']}"),
        ("search_job", lambda item: item["search_job"]),
        ("suspected_class", lambda item: item["suspected_class"]),
        ("evidence_profile", lambda item: item["evidence_profile"]),
        ("contact_route_profile", lambda item: item["contact_route_profile"]),
    )

    for _, getter in dimensions:
        groups: dict[str, list[dict[str, Any]]] = defaultdict(list)
        for annotation in annotations:
            groups[str(getter(annotation) or "unknown")].append(annotation)
        for key in sorted(groups):
            if len(selected) >= sample_size:
                break
            pick = _first_unselected(groups[key], selected, seed)
            if pick:
                selected[pick["candidate_id"]] = pick
        if len(selected) >= sample_size:
            break

    counts: dict[str, Counter[str]] = {name: Counter() for name, _ in dimensions}
    for item in selected.values():
        for name, getter in dimensions:
            counts[name][str(getter(item) or "unknown")] += 1

    while len(selected) < sample_size:
        remaining = [item for item in annotations if item["candidate_id"] not in selected]
        if not remaining:
            break
        remaining.sort(
            key=lambda item: (
                -_diversity_score(item, dimensions=dimensions, counts=counts),
                _stable_hash(f"{seed}:{item['candidate_id']}"),
            )
        )
        pick = remaining[0]
        selected[pick["candidate_id"]] = pick
        for name, getter in dimensions:
            counts[name][str(getter(pick) or "unknown")] += 1

    return sorted(selected.values(), key=lambda item: (_stable_hash(f"{seed}:{item['candidate_id']}"), item["candidate_id"]))


def _first_unselected(items: list[dict[str, Any]], selected: dict[str, dict[str, Any]], seed: str) -> dict[str, Any] | None:
    for item in sorted(items, key=lambda value: (_stable_hash(f"{seed}:{value['candidate_id']}"), value["candidate_id"])):
        if item["candidate_id"] not in selected:
            return item
    return None


def _diversity_score(
    item: dict[str, Any],
    *,
    dimensions: tuple[tuple[str, Callable[[dict[str, Any]], str]], ...],
    counts: dict[str, Counter[str]],
) -> float:
    score = 0.0
    for name, getter in dimensions:
        value = str(getter(item) or "unknown")
        score += 1.0 / (1 + counts[name][value])
    return score


